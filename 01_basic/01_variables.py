# -----------------------------
# Example 1: Creating Variables
# -----------------------------

name = "Manas"
age = 21
height = 5.8
is_student = True

print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student:", is_student)

print("-" * 40)

# -----------------------------
# Example 2: Multiple Assignment
# -----------------------------

city, state, country = "Durg", "Chhattisgarh", "India"

print(city)
print(state)
print(country)

print("-" * 40)

# -----------------------------
# Example 3: Assign Same Value
# -----------------------------

x = y = z = 100

print(x)
print(y)
print(z)

print("-" * 40)

# -----------------------------
# Example 4: Updating Variables
# -----------------------------

score = 80
print("Old Score:", score)

score = 95
print("Updated Score:", score)

print("-" * 40)

# -----------------------------
# Example 5: Variable Naming
# -----------------------------

student_name = "Rahul"
student_age = 20
college_name = "KK Modi University"

print(student_name)
print(student_age)
print(college_name)

print("-" * 40)

# -----------------------------
# Example 6: Check Data Type
# -----------------------------

number = 50
price = 99.99
language = "Python"

print(type(number))
print(type(price))
print(type(language))

print("-" * 40)

